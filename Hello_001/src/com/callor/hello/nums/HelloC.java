package com.callor.hello.nums;

public class HelloC {
	public static void main(String[] args) {
		// +,-,*,/ 기호는 연산자 (operator)라 부른다
		System.out.println(30 + 40);
		System.out.println(30 - 40);
		System.out.println(30 * 40);
		System.out.println(30 / 40); // 정수 나눗셈은 몫을 구한다.
		System.out.println(30 % 40); // % 연산은 나머지를 구한다.
	}
}
