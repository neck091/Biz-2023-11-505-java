package com.callor.hello.start;

public class HelloD {
	public static void main(String[] args) {
		
		// 변수는 값을 보관하거나 읽기 전에
		// 반드시 먼저 선언(예약)해야 한다
		int num1 = 0;
		int num2 = 0;
		
		// 변수에 값 보관하기
		num1 = 30;
		num2 = 40;
		
		
		// 변수에 값 읽기
		System.out.println(num1 + num2);
		System.out.println(num1 - num2);
		System.out.println(num1 * num2);
		System.out.println(num1 / num2);
		
	}

}
